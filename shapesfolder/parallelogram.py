"""Parallelogram

Provides functions to calculate perimeter and area for a parallelogram.

"""

def perimeter(l,sl):
  """Calculates the perimeter given the base length and side length.
  
  Parameters:
  ----------
  l,sl

  Returns:
  ----------
  perimeter
  """
  return (l+sl)*2

def area(l,h):
  """Calculates the area given the width and height.
  
  Parameters:
  ----------
  l,h

  Returns:
  ----------
  area
  """
  return l*h