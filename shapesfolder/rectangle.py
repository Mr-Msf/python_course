"""Rectangle

Provides functions to calculate perimeter and area for a rectangle.

"""

def perimeter(l,w):
  """Calculates the perimeter given the length and width.
  
  Parameters:
  ----------
  l,w

  Returns:
  ----------
  perimeter
  """
  return (l+w)*2

def area(l,w):
  """Calculates the area given the length and width.
  
  Parameters:
  ----------
  l,w

  Returns:
  ----------
  area
  """
  return l*w