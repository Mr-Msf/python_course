"""Square

Provides functions to calculate perimeter and area for a square.

"""

def perimeter(l):
  """Calculates the perimeter given the length.
  
  Parameters:
  ----------
  l

  Returns:
  ----------
  perimeter
  """
  return l*4

def area(l):
  """Calculates the area given the length.
  
  Parameters:
  ----------
  l

  Returns:
  ----------
  area
  """
  return l*l